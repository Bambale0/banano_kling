def format_preset_info(preset) -> str:
    """Форматирует информацию о пресете для пользователя"""
    info = f"🎯 <b>{preset.name}</b>"
    info += f"🍌 Стоимость: <code>{preset.cost}</code>🍌\n"
    info += f"🤖 Модель: <code>{preset.model}</code>\n"

    if hasattr(preset, "description") and preset.description:
        info += f"\n📝 {preset.description}\n"

    if preset.aspect_ratio:
        info += f"📐 Формат: <code>{preset.aspect_ratio}</code>\n"

    if preset.requires_upload:
        info += "\n📎 <i>Требуется загрузить фото</i>\n"

    if preset.requires_input and preset.input_prompt:
        info += f"\n💭 {preset.input_prompt}\n"

    return info


def format_generation_options(options: dict) -> str:
    """Форматирует выбранные опции генерации"""
    text = "⚙️ <b>Выбранные опции:</b>"

    if options.get("model"):
        emoji = "💎" if "pro" in options["model"] else "⚡"
        text += f"{emoji} Модель: <code>{options['model']}</code>\n"

    if options.get("aspect_ratio"):
        text += f"📐 Формат: <code>{options['aspect_ratio']}</code>\n"

    if options.get("resolution"):
        text += f"👁 Разрешение: <code>{options['resolution']}</code>\n"

    if options.get("enable_search"):
        text += f"🔍 Поиск: <code>ВКЛ</code>\n"

    if options.get("reference_count"):
        text += f"🖼 Референсы: <code>{options['reference_count']}</code>\n"

    return text
